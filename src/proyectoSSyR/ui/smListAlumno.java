    private void btnBuscarCampoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCampoActionPerformed
     String camp=(String) showInputDialog(jpContenedor, "Selecione como se realizara la busqueda","Busqueda por Campo", WIDTH, icon, campo, campo[0]);
     String palabra=(String) showInputDialog(jpContenedor, "Introduce "+camp+" Alumno","Busqueda por "+camp, WIDTH, icon, null, accessibleContext);   
       if (palabra.equals("No Control")) {
            ListAlumnoJP.buscar(palabra,1);
        }
        if (camp.equals("Nombre รณ Apellido")) {
            ListAlumnoJP.buscar(palabra,2);
        }
        if (camp.equals("Carrera")) {
            ListAlumnoJP.buscar(palabra,3);
        }
        if (camp.equals("Curricula")) {
            ListAlumnoJP.buscar(palabra,4);
        }
        
            
     System.out.println(palabra);
    }//GEN-LAST:event_btnBuscarCampoActionPerformed

